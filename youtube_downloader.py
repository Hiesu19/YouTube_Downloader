	def check(self):
		url = self.get_link.text()
		try:
			title_msg = YouTube(url).title
			views_msg = str(YouTube(url).views)
			thum_img = YouTube(url).thumbnail_url
			self.title_box.setText(title_msg)
			self.view_box.setText(views_msg)
			self.thumbnail_box.setText(thum_img)
			self.label_10.setText("         Checked")

		except Exception as e:
			self.label_10.setText("ERROR:  Link Video unavailable ")
			self.title_box.setText("......")
			self.view_box.setText("......")
			self.thumbnail_box.setText("......")

	def download_bt(self):
		opti = self.comboBox.currentText()
		url = self.get_link.text()
		save_url = self.save.text()
		self.lineEdit.setText("   LOADING......")
		try:
			title_msg = YouTube(url).title
		except Exception as e:
			self.label_10.setText("ERROR:  Link Video unavailable to download ")
			self.lineEdit.setText("   Oh no :(((")
			return
		if opti == "video 360p":
			itag = "18"
			YouTube(url).streams.get_by_itag(itag).download(save_url)
			self.label_10.setText("     Finish   video 360p")
		if opti == "video 720p":
			itag = "22"
			YouTube(url).streams.get_by_itag(itag).download(save_url)
			self.label_10.setText("     Finish   video 720p")
		if opti == "video 1080p":
			itag = "137"
			YouTube(url).streams.get_by_itag(itag).download(save_url)
			self.label_10.setText("     Finish   video 1080p")
		if opti == "audio 128 kbps":
			itag = "140"
			YouTube(url).streams.get_by_itag(itag).download(save_url)
			self.label_10.setText("     Finish   audio 128 kbps")
		if opti == "audio 160 kbps":
			itag = "251"
			YouTube(url).streams.get_by_itag(itag).download(save_url)
			self.label_10.setText("     Finish   audio 160 kbps")
		mini_win = QtWidgets.QMessageBox()
		mini_win.setIcon(QtWidgets.QMessageBox.Information)
		mini_win.setText("Finish:  " + title_msg)
		if save_url =="":
			mini_win.setInformativeText("Save in : This file  ")
		else:
			mini_win.setInformativeText("Save in : " + save_url)
		mini_win.setWindowTitle("Chi ph√≠")
		mini_win.exec_()


